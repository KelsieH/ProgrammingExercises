public class Rainfall
{
    public static void main ( String[] args )
    {
        int april = 12;
        int may = 14;
        int june = 8;
        double average = (april + may + june)/3;
        System.out.println("Rainfall for April: " + april + "\t Rainfall for May: " + may + "\t Rainfall for June: " + june + "\t Average Rainfall: " + average);
    }
}